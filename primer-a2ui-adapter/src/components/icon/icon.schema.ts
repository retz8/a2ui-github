/**
 * Runtime (zod) representation of Icon, props-only.
 *
 * Icon is a synthetic component: no single component of this name exists in
 * `@primer/octicons-react`. The render resolves `name` to the matching named export.
 *
 * - `name` is the required synthetic content prop: the icon selected by name from the
 *   fixed set (the `ICON_NAMES` enum).
 * - `size`/`fill` are Primer's real prop surface, deliberately tightened to enums (the
 *   numeric `size` and raw-color-string `fill` halves are dropped; `fill` becomes a
 *   semantic-role enum mapped to design-system tokens at render).
 * - `accessibility` maps `label` -> `aria-label` (the icon then exposes `role="img"`);
 *   with no label the icon renders `aria-hidden` (decorative).
 *
 * `.strict()` forbids any prop outside this surface.
 */
export const IconApi = {
  name: 'Icon',
  schema: z
    .object({
      name: z.enum(ICON_NAMES),
      size: z.enum(['small', 'medium', 'large']).optional(),
      fill: z.enum(ICON_FILLS).optional(),
      accessibility: CommonSchemas.AccessibilityAttributes.optional(),
    })
    .strict(),
} as const;

export type IconProps = z.infer<typeof IconApi.schema>;
